export const DebounceSearch = (fn, delay = 500) => {
  let timer;

  const debounced = (...args) => {
    // cleared previous timer
    clearTimeout(timer);
    //   started new one timer
    timer = setTimeout(() => {
      // Calling function with latest function
      fn(...args);
    }, delay);
  };

  debounced.cancel = () => {
    clearTimeout(timer);
  };

  return debounced;
};
